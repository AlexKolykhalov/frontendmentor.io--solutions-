// @ts-check

/**
 * @type {HTMLButtonElement|null}
 */
const toggle = document.querySelector('.navigation-toggle');

/**
 * @type {HTMLUListElement|null}
 */
const navigation = document.querySelector('.navigation');

/**
 * @type {HTMLDivElement|null}
 */
const filter = document.querySelector('.filter');


// **********************************************************************//
// ***********************  Table of content  ***************************//
// **********************************************************************//
// 
// 1. Events
//  1.1 Toggle
//  
// 2. Functions

// **********************************************************************//
// ****************************  Events  ********************************//
// **********************************************************************//


// *************************** 1.1 Toggle *********************************//
// **********************************************************************//

toggle?.addEventListener('click', () => {
    const isOpen = navigation?.getAttribute('data-visible');

    navigation?.setAttribute('data-visible', isOpen == 'true' ? 'false' : 'true');
    toggle.setAttribute('data-visible', isOpen == 'true' ? 'false' : 'true');
    filter?.setAttribute('data-visible', isOpen == 'true' ? 'false' : 'true');
    toggle.setAttribute('aria-expanded', isOpen == 'true' ? 'false' : 'true');
});


